void GpioBoard(
		u32_t dwPin,
		StatusPeripheral status
)
{
		switch(status)
		{
			case HIGH:		GPIO_PinHigh(dwPin);
				break;
			case LOW:			GPIO_PinLow(dwPin);
				break;
			case TOGGLE:	GPIO_PinToggle(dwPin);	
				break;
			default:
				break;
		}
}
